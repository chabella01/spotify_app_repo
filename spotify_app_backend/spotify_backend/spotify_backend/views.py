@api_view(['GET'])
def authorize_spotify(request):
    scopes = 'user-read-playback user-read-currently-playing user-modify-playback-state'
    url = Request('GET', 'https://acounts.spotify.com/authorize', params={
            'scope': scopes,
            'response_type': 'code',
            'redirect_uri': REDIRECT_URI,
            'client_id': CLIENT_ID
        }).prepare().url

    return Response({'url': url}, status=status.HTTP_200_OK)

